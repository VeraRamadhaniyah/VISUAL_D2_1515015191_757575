    private void searchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyTyped
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        String by = combosearch.getSelectedItem().toString();
        try{
            String sql = "SELECT * FROM rumahsakit WHERE "+by+" like '%"+search.getText()+"%'";
            stt = con.createStatement();
            rss =stt.executeQuery(sql);
            ResultSet rss =stt.executeQuery(sql);
            while (rss.next()){
                Object[] o = new Object[8];
                o [0] = rss.getString("nama_pasien");
                o [1] = rss.getDate("tl");
                o [2] = rss.getInt("usia");
                o [3] = rss.getString("jk");
                o [4] = rss.getString("diagnosa");
                o [5] = rss.getString("dokter");
                o [6] = rss.getString("ruangan");
                o [7] = rss.getDate("tgl_masuk");
                model.addRow(o);
            }
            stt.close();
            rss.close();
        }catch(SQLException e){
            System.out.println("Terjadi Kesalahan");
        }
        
    }//GEN-LAST:event_searchKeyTyped

