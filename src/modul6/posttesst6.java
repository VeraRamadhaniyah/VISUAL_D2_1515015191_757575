     private void TampilData(){
         try{
            String sql = "SELECT * FROM rumahsakit";
            stt = con.createStatement();
            rss = stt.executeQuery(sql);
            while(rss.next()){
                Object[] o = new Object[8];
                o[0] = rss.getString("nama_pasien");
                o[1] = rss.getDate("tl");
                o[2] = rss.getInt("usia");
                o[3] = rss.getString("jk");
                o[4] = rss.getString("diagnosa");
                o[5] = rss.getString("dokter");
                o[6] = rss.getString("ruangan");
                o[7] = rss.getDate("tgl_masuk");
                model.addRow(o);
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        
         }
     }
     
     private void TampilDokter(){
         DefaultComboBoxModel combodokter = new DefaultComboBoxModel();
         comboDokter.setModel(combodokter);
         
         try{
            String sql = "SELECT * FROM dokter";
            stt = con.createStatement();
            rss = stt.executeQuery(sql);
            while(rss.next()){
                combodokter.addElement(rss.getString("namadokter"));
            }
          } catch (SQLException e) {
            System.out.println(e.getMessage());
        
         }
     }
     
     private void TambahData(String nama_pasien, String tl, String usia, String jk,
             String diagnosa, String dokter, String ruangan, String tgl_masuk){
         try{
             String sql =
                     "INSERT INTO rumahsakit VALUES (NULL,'"+
                     nama_pasien+"','"+tl+"',"+usia+",'"+jk+"','"+diagnosa+"','"+dokter+"','"+
                     ruangan+"','"+tgl_masuk+"')";
              stt = con.createStatement();
              stt.executeUpdate(sql);   
        }catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
